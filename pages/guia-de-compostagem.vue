          <p>
            <strong>
              A composteira deve ficar em um local arejado e protegido do sol e
              da chuva.
            </strong>
          </p>
          <ol>
            <li>
              Coloque o suporte no local escolhido, ou use o suporte já
              existente (degrau, móvel, etc).
            </li>
            <li>
              Instale a torneira para o lado de fora, cuidando para colocar um
              anel de vedação pelo lado de fora e outro pelo lado de dentro.
              Atenção para deixar a torneira na posição fechada (para trás).
            </li>
            <li>
              Coloque a caixa coletora (com a torneira) em cima do suporte. Para
              facilitar a retirada do composto líquido pela torneira, coloque um
              pequeno calço embaixo da caixa na extremidade oposta à torneira.
              Nessa mesma extremidade, pela parte de dentro, sugerimos colocar
              um tijolo encostado na parede interna da caixa. Esse tijolo
              ajudará as minhocas que caírem nessa caixa a saírem do líquido e
              retornarem para a caixa do meio.
            </li>
            <li>
              Encaixe a primeira caixa digestora em cima da caixa coletora.
            </li>
            <li>
              Espalhe o conteúdo do pacote de composto, húmus e serragem para
              forrar o fundo dessa caixa digestora (caixa do meio).
            </li>
            <li>Encaixe a segunda caixa digestora em cima da primeira.</li>
            <li>
              Espalhe o conteúdo do pacote de minhocas e substrato para forrar o
              fundo dessa caixa digestora (caixa de cima).
            </li>
            <li>Coloque a tampa nessa caixa.</li>
          </ol>
