            diariamente o equivalente ao seu peso em matéria orgânica, duplicam
            a população a cada 2 meses e diminuem o ritmo de reprodução quando
            percebem que o espaço ficou pequeno para a quantidade de indivíduos.
          </p>
          <p>
            O sistema produz adubos de excelente qualidade (composto sólido e
            composto líquido) e, corretamente manuseado, não produz cheiro nem
            atrai animais indesejáveis.
