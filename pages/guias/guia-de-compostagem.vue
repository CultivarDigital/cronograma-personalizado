            <p>
              O composto sólido é uma mistura de resíduos em decomposição
              avançada e húmus de minhoca que quando pronto, possui aspecto de
              terra preta.
            </p>
            <p>
              Para a sua retirada, no momento da troca das caixas digestoras,
              coloque a caixa do meio aberta ao sol. Devido à intensidade da
              luz, as minhocas mergulharão no composto. Raspe o adubo aos
              poucos. Repita esse procedimento até a camada de adubo ficar com 5
              a 7cm de altura ou com grande concentração de minhocas. Deixe essa
              camada na caixa. Se ela estiver muito compactada, misture o
              composto levemente para não machucar as minhocas.
            </p>
