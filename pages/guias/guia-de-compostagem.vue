            <h3>USANDO OS COMPOSTOS</h3>
            <h4 class="text-brown3">COMPOSTO SÓLIDO</h4>
            <p>
              Você pode usar o composto sólido tanto para adubar diretamente as
              plantas como para revitalizar vasos e melhorar terras
              enfraquecidas a serem usadas em novos plantios .
            </p>
            <h5 class="text-brown">PARA ADUBAR AS PLANTAS</h5>
            <p>
              Faça alguns furos na terra com uma ferramenta pontiaguda. Coloque
              alguns punhados do composto no ‘pé’ da planta até cobrir a
              superfície correspondente à “copa” da mesma com uma camada de 2 a
              3 centímetros. Com a chuva ou rega, os nutrientes descerão para a
              terra, que ficará coberta pela matéria vegetal seca presente no
              composto. Esta cobertura preservará a umidade e os nutrientes
              recém absorvidos pela terra.
            </p>
            <h5 class="text-brown">
              PARA REVITALIZAR VASOS E MELHORAR TERRAS FRACAS
            </h5>
            <p>
              Faça uma mistura do composto + areia média + terra anterior
              aproximadamente na mesma proporção. Após a homogene- ização da
              mistura, coloque-a novamente no local a ser plantado. Em caso de
              vasos, coloque argila expandida no fundo e separe-a da mistura com
              um pedaço de tecido para facilitar a drenagem da água.
            </p>
            <h4 class="text-brown3">COMPOSTO LÍQUIDO</h4>
            <p>
              O composto líquido é um excelente adubo! As plantas absorvem seus
              nutrientes com muita facilidade e rapidamente apresentam
              resultados. Por ser muito rico em nutrientes, ele deve ser diluído
              na proporção de 1 parte de composto líquido para 10 partes de
              água, e a adubação não pode ser diária. Recomendamos um intervalo
              de pelo menos uma semana entre as aplicações na mesma planta.
            </p>
            <h5 class="text-brown">
              FORMAS DE USAR O COMPOSTO LÍQUIDO DILUÍDO NA PROPORÇÃO DE 1/10
            </h5>
            <p>
              <strong>Rega:</strong> Faça a diluição em um regador e regue as
              plantas.
            </p>
            <p>
              <strong>Adubação Foliar:</strong> Coloque a diluição em um
              borrifador e aplique nas folhas das plantas.
